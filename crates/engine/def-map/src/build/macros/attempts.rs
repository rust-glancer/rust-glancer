/// Selects which part of the macro worklist one expansion pass should inspect.
#[derive(Debug, Clone, Copy)]
pub(crate) enum MacroExpansionScan<'a> {
    AllPending,
    NewCallsSince(&'a MacroExpansionCursors),
}

impl MacroExpansionScan<'_> {
    fn start_for(self, target: TargetRef) -> usize {
        match self {
            Self::AllPending => 0,
            Self::NewCallsSince(cursors) => cursors.start_for(target),
        }
    }
}

fn should_scan_directive(state: MacroDirectiveState, scan: MacroExpansionScan<'_>) -> bool {
    match state {
        MacroDirectiveState::Pending => true,
        // Unresolved calls are worth revisiting only after imports have been refreshed. Generated
        // items may introduce new imports or macro definitions, but repeated inner-loop scans see
        // the same scope snapshot and would only reproduce the same miss.
        MacroDirectiveState::Unresolved => matches!(scan, MacroExpansionScan::AllPending),
        MacroDirectiveState::Expanded
        | MacroDirectiveState::Failed
        | MacroDirectiveState::Skipped
        | MacroDirectiveState::IgnoredByDefMap
        | MacroDirectiveState::Unsupported => false,
    }
}

/// Per-target cursors used when only macro calls generated by the previous pass should be scanned.
#[derive(Debug, Clone, Default)]
pub(crate) struct MacroExpansionCursors {
    macro_call_counts: HashMap<TargetRef, usize>,
}

impl MacroExpansionCursors {
    pub(crate) fn capture(states: &FinalizeTargetStates) -> Self {
        let mut macro_call_counts = HashMap::new();

        for package_states in states.iter_dirty() {
            for state in package_states {
                macro_call_counts.insert(state.target, state.macro_directives.len());
            }
        }

        Self { macro_call_counts }
    }

    fn start_for(&self, target: TargetRef) -> usize {
        self.macro_call_counts.get(&target).copied().unwrap_or(0)
    }
}

/// Resolves pending macro calls into concrete attempts for the current scope snapshot.
pub(crate) fn collect_expansion_attempts<E>(
    env: &E,
    states: &FinalizeTargetStates,
    scan: MacroExpansionScan<'_>,
    cache: &mut MacroExpansionCache,
) -> anyhow::Result<Vec<MacroExpansionAttempt>>
where
    E: TargetResolutionEnv<Error = rg_package_store::PackageStoreError> + MacroDefinitionEnv,
{
    let mut attempts = Vec::new();

    for package_states in states.iter_dirty() {
        for state in package_states {
            // A generated expansion pass only needs to inspect calls appended after the cursor.
            // Existing unresolved calls are revisited after imports have been refreshed.
            for (call_id, directive) in state
                .macro_directives
                .iter()
                .enumerate()
                .skip(scan.start_for(state.target))
            {
                if !should_scan_directive(directive.state, scan) {
                    continue;
                }

                let attempt = MacroExpansionAttempt::for_call(
                    env,
                    states,
                    cache,
                    state,
                    call_id,
                    &directive.call,
                )
                .with_context(|| {
                    format!(
                        "while attempting to inspect macro call in {}",
                        state.target_name
                    )
                })?;
                attempt.record();
                attempts.push(attempt);
            }
        }
    }

    Ok(attempts)
}

/// Applies expansion results to target state and returns whether new scope facts were added.
pub(crate) fn apply_expansion_attempts(
    item_tree: &ItemTreeDb,
    states: &mut FinalizeTargetStates,
    interners: &mut PackageNameInterners,
    current_scopes: &mut ScopeMatrix,
    attempts: Vec<MacroExpansionAttempt>,
) -> anyhow::Result<MacroExpansionApplyResult> {
    let mut result = MacroExpansionApplyResult::default();

    for attempt in attempts {
        let Some(state) = states.target_mut(attempt.target) else {
            continue;
        };

        let syntax = match attempt.outcome {
            MacroExpansionAttemptOutcome::NoSource(directive_state) => {
                if let Some(directive) = state.macro_directives.get_mut(attempt.call_id) {
                    directive.state = directive_state;
                }
                continue;
            }
            MacroExpansionAttemptOutcome::Generated(source) => source,
            MacroExpansionAttemptOutcome::ItemTreeFile(file_id) => {
                let item_tree_package = item_tree.package(attempt.target.package.0).with_context(
                    || {
                        format!(
                            "while attempting to fetch item tree package {} for include expansion",
                            attempt.target.package.0
                        )
                    },
                )?;
                let collected = SourceFragmentCollector {
                    state,
                    current_scopes,
                    item_tree: item_tree_package,
                    origin: SourceFragmentOrigin {
                        module: attempt.origin.module,
                        order: attempt.origin.order,
                    },
                    result: MacroExpansionApplyResult::default(),
                }
                .collect_file(file_id);
                let directive_state = match collected {
                    Ok(collected) => {
                        result.merge(collected);
                        MacroDirectiveState::Expanded
                    }
                    Err(_) => MacroDirectiveState::Failed,
                };
                if let Some(directive) = state.macro_directives.get_mut(attempt.call_id) {
                    directive.state = directive_state;
                }
                continue;
            }
            MacroExpansionAttemptOutcome::ItemTreeFragment { file_id, items } => {
                let item_tree_package = item_tree.package(attempt.target.package.0).with_context(
                    || {
                        format!(
                            "while attempting to fetch item tree package {} for builtin fragment",
                            attempt.target.package.0
                        )
                    },
                )?;
                let collected = SourceFragmentCollector {
                    state,
                    current_scopes,
                    item_tree: item_tree_package,
                    origin: SourceFragmentOrigin {
                        module: attempt.origin.module,
                        order: attempt.origin.order,
                    },
                    result: MacroExpansionApplyResult::default(),
                }
                .collect_fragment(file_id, &items);
                let directive_state = match collected {
                    Ok(collected) => {
                        result.merge(collected);
                        MacroDirectiveState::Expanded
                    }
                    Err(_) => MacroDirectiveState::Failed,
                };
                if let Some(directive) = state.macro_directives.get_mut(attempt.call_id) {
                    directive.state = directive_state;
                }
                continue;
            }
            MacroExpansionAttemptOutcome::PendingExpansion(_) => {
                unreachable!("macro expansion work should be executed before generated apply");
            }
        };
        let macro_name = attempt.macro_name;
        // Generated items are collected with the target's normal name interner so later stages see
        // the same names and source references as they do for ordinary items.
        let interner = interners
            .package_mut(attempt.target.package.0)
            .with_context(|| {
                format!(
                    "while attempting to fetch name interner for package {}",
                    attempt.target.package.0
                )
            })?;
        let collected = GeneratedCollector {
            state,
            interner,
            current_scopes,
            origin: attempt.origin,
            result: MacroExpansionApplyResult::default(),
        }
        .collect_syntax(syntax, macro_name.as_deref());
        let directive_state = match collected {
            Ok(collected) => {
                result.merge(collected);
                MacroDirectiveState::Expanded
            }
            Err(_) => MacroDirectiveState::Failed,
        };
        if let Some(directive) = state.macro_directives.get_mut(attempt.call_id) {
            directive.state = directive_state;
        }
    }

    Ok(result)
}

/// Whether applying a batch of expansion attempts changed the current def-map state.
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub(crate) struct MacroExpansionApplyResult {
    pub(crate) changed: bool,
}

impl MacroExpansionApplyResult {
    pub(super) fn mark_changed(&mut self) {
        self.changed = true;
    }

    fn merge(&mut self, other: Self) {
        self.changed |= other.changed;
    }
}

/// One inspected macro directive, including the work/result and stats events it produced.
pub(crate) struct MacroExpansionAttempt {
    target: TargetRef,
    call_id: usize,
    macro_name: Option<String>,
    origin: GeneratedOrigin,
    outcome: MacroExpansionAttemptOutcome,
    record: MacroExpansionAttemptRecord,
}

impl MacroExpansionAttempt {
    /// The call shape is not usable by item-position macro expansion.
    fn skipped(target: TargetRef, call_id: usize, call: &MacroCallSite) -> Self {
        Self::new(
            target,
            call_id,
            call,
            call.path
                .clone()
                .or_else(|| call.callee.as_ref().map(ToString::to_string)),
            MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Skipped),
            MacroExpansionAttemptRecord::skipped(),
        )
    }

    /// A builtin resolved, but it cannot add module-scope items for def-map.
    fn ignored_by_def_map(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        path_text: &str,
    ) -> Self {
        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::IgnoredByDefMap),
            MacroExpansionAttemptRecord::skipped(),
        )
    }

    /// A known builtin needs dedicated support before def-map can model it safely.
    fn unsupported_builtin(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        path_text: &str,
    ) -> Self {
        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Unsupported),
            MacroExpansionAttemptRecord::skipped(),
        )
    }

    /// Builtin `include!` splices a real file that item-tree already lowered.
    fn include_file(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        path_text: &str,
    ) -> Self {
        let Some(BuiltinMacroItem::Include { file }) = call.builtin.as_ref() else {
            return Self::unsupported_builtin(target, call_id, call, path_text);
        };

        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            MacroExpansionAttemptOutcome::ItemTreeFile(*file),
            MacroExpansionAttemptRecord::builtin_expanded(),
        )
    }

    /// Builtin `cfg_select!` chooses one item stream using the target cfg environment.
    fn cfg_select(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        state: &TargetState,
        path_text: &str,
    ) -> Self {
        let Some(BuiltinMacroItem::CfgSelect { arms }) = call.builtin.as_ref() else {
            return Self::new(
                target,
                call_id,
                call,
                Some(path_text.to_string()),
                MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Failed),
                MacroExpansionAttemptRecord::builtin_failed(),
            );
        };
        let cfg = state.cfg_evaluator();
        let Some(arm) = arms
            .iter()
            .find(|arm| cfg.is_predicate_enabled(&arm.predicate))
        else {
            return Self::new(
                target,
                call_id,
                call,
                Some(path_text.to_string()),
                MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Failed),
                MacroExpansionAttemptRecord::builtin_failed(),
            );
        };

        let (outcome, record) = match &arm.payload {
            CfgSelectArmPayload::Items(items) => (
                MacroExpansionAttemptOutcome::ItemTreeFragment {
                    file_id: call.file_id,
                    items: items.clone(),
                },
                MacroExpansionAttemptRecord::builtin_expanded(),
            ),
            CfgSelectArmPayload::LoweringFailed => (
                MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Failed),
                MacroExpansionAttemptRecord::builtin_failed(),
            ),
        };

        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            outcome,
            record,
        )
    }

    /// The call may become resolvable after another import-resolution refresh.
    fn unresolved(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        path_text: &str,
    ) -> Self {
        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Unresolved),
            MacroExpansionAttemptRecord::unresolved(),
        )
    }

    /// A declarative macro resolved and produced compile/expand accounting.
    fn resolved(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        path_text: &str,
        outcome: MacroExpansionAttemptOutcome,
        compile: MacroCompileRecord,
        expand: Option<MacroExpandRecord>,
    ) -> Self {
        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            outcome,
            MacroExpansionAttemptRecord::resolved(compile, expand),
        )
    }

    /// A direct same-module macro resolved but is declared after this call.
    fn resolved_skipped(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        path_text: &str,
    ) -> Self {
        Self::new(
            target,
            call_id,
            call,
            Some(path_text.to_string()),
            MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Skipped),
            MacroExpansionAttemptRecord::resolved_skipped(),
        )
    }

    /// Common initializer that anchors generated items at the original call site.
    fn new(
        target: TargetRef,
        call_id: usize,
        call: &MacroCallSite,
        macro_name: Option<String>,
        outcome: MacroExpansionAttemptOutcome,
        record: MacroExpansionAttemptRecord,
    ) -> Self {
        Self {
            target,
            call_id,
            macro_name,
            origin: GeneratedOrigin {
                module: call.module,
                source: call.source,
                file_id: call.file_id,
                span: call.span,
                order: call.order.clone(),
                dollar_crate_target: None,
            },
            outcome,
            record,
        }
    }

    fn for_call<E>(
        env: &E,
        states: &FinalizeTargetStates,
        cache: &mut MacroExpansionCache,
        state: &TargetState,
        call_id: usize,
        call: &MacroCallSite,
    ) -> anyhow::Result<Self>
    where
        E: TargetResolutionEnv<Error = rg_package_store::PackageStoreError> + MacroDefinitionEnv,
    {
        // First normalize the syntactic call into a path and argument list. Calls that are not
        // item-position macro invocations are marked done so the worklist can move on.
        let Some(path_text) = call.path.as_deref().or(call.callee.as_deref()) else {
            return Ok(Self::skipped(state.target, call_id, call));
        };
        let Some(args) = call.args.as_ref() else {
            return Ok(Self::skipped(state.target, call_id, call));
        };
        let Some(path) = ImportPath::from_macro_path_text(path_text, call.dollar_crate_target)
        else {
            return Ok(Self::skipped(state.target, call_id, call));
        };

        // Then resolve against the current scope snapshot. Unresolved user macros stay resumable;
        // known builtins are classified once so we do not retry names that def-map cannot expand.
        let resolver = ItemMacroResolver::new(env, states, state);
        let Some(resolved) = resolver.resolve(call, &path)? else {
            match BuiltinMacroDisposition::from_path(&path) {
                Some(BuiltinMacroDisposition::IgnoredByDefMap) => {
                    return Ok(Self::ignored_by_def_map(
                        state.target,
                        call_id,
                        call,
                        path_text,
                    ));
                }
                Some(BuiltinMacroDisposition::CfgSelect) => {
                    return Ok(Self::cfg_select(
                        state.target,
                        call_id,
                        call,
                        state,
                        path_text,
                    ));
                }
                Some(BuiltinMacroDisposition::Include) => {
                    return Ok(Self::include_file(state.target, call_id, call, path_text));
                }
                Some(BuiltinMacroDisposition::Unsupported) => {
                    return Ok(Self::unsupported_builtin(
                        state.target,
                        call_id,
                        call,
                        path_text,
                    ));
                }
                None => return Ok(Self::unresolved(state.target, call_id, call, path_text)),
            }
        };

        // Direct `macro_rules!` bindings cannot be used before a later definition in the same
        // module. Imported bindings and `#[macro_export]` root bindings are path-based and have
        // already gone through ordinary scope resolution.
        if resolved.origin == ScopeBindingOrigin::Direct
            && resolved.def_ref.origin == DefMapRef::Target(state.target)
            && resolved.local_def.module == call.module
            && let Some(order) = resolved.order
            && order > &call.order
        {
            return Ok(Self::resolved_skipped(
                state.target,
                call_id,
                call,
                path_text,
            ));
        }

        // Finally compile the definition and either reuse cached output or prepare self-contained
        // expansion work for the worker pool.
        let edition = macro_edition(resolved.data.edition);
        let compile_result = cache.compile(resolved.def_ref, resolved.data, edition);
        let Some(macro_) = compile_result.macro_ else {
            return Ok(Self::resolved(
                state.target,
                call_id,
                call,
                path_text,
                MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Failed),
                compile_result.record,
                None,
            ));
        };

        let call_site =
            tt_span_for_parse_span(call.file_id, call.span, macro_edition(state.edition));
        let prepared_expansion = cache.prepare_expansion(
            resolved.def_ref,
            macro_,
            path_text,
            args,
            call_site,
            ExpansionParseKind::Items,
        );
        let outcome = match prepared_expansion.expansion {
            PreparedMacroExpansion::Syntax(syntax) => {
                MacroExpansionAttemptOutcome::Generated(syntax)
            }
            PreparedMacroExpansion::Failed => {
                MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Failed)
            }
            PreparedMacroExpansion::Work(work) => {
                MacroExpansionAttemptOutcome::PendingExpansion(work)
            }
        };

        let mut attempt = Self::resolved(
            state.target,
            call_id,
            call,
            path_text,
            outcome,
            compile_result.record,
            Some(prepared_expansion.record),
        );
        attempt.origin.dollar_crate_target = Some(resolved.data.dollar_crate_target);
        Ok(attempt)
    }

    pub(crate) fn needs_expansion(&self) -> bool {
        matches!(
            self.outcome,
            MacroExpansionAttemptOutcome::PendingExpansion(_)
        )
    }

    fn record(&self) {
        let macro_name = self.macro_name.as_deref().unwrap_or("<unknown>");
        self.record.record(macro_name);
    }

    pub(super) fn take_expansion_work(&mut self) -> Option<MacroExpansionWork> {
        let outcome = std::mem::replace(
            &mut self.outcome,
            MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Pending),
        );
        match outcome {
            MacroExpansionAttemptOutcome::PendingExpansion(work) => Some(work),
            other => {
                self.outcome = other;
                None
            }
        }
    }

    pub(super) fn set_expansion_result(&mut self, syntax: Option<ExpansionSyntax>) {
        self.outcome = match syntax {
            Some(syntax) => MacroExpansionAttemptOutcome::Generated(syntax),
            None => MacroExpansionAttemptOutcome::NoSource(MacroDirectiveState::Failed),
        };
    }
}

/// Accounting events produced while classifying and preparing one macro attempt.
#[derive(Debug, Clone, Copy, Default)]
struct MacroExpansionAttemptRecord {
    resolved: bool,
    unresolved: bool,
    skipped: bool,
    expanded: bool,
    failed: bool,
    compile: Option<MacroCompileRecord>,
    expand: Option<MacroExpandRecord>,
}

impl MacroExpansionAttemptRecord {
    fn skipped() -> Self {
        Self {
            skipped: true,
            ..Self::default()
        }
    }

    fn unresolved() -> Self {
        Self {
            unresolved: true,
            ..Self::default()
        }
    }

    fn resolved(compile: MacroCompileRecord, expand: Option<MacroExpandRecord>) -> Self {
        Self {
            resolved: true,
            compile: Some(compile),
            expand,
            ..Self::default()
        }
    }

    fn builtin_expanded() -> Self {
        Self {
            resolved: true,
            expanded: true,
            ..Self::default()
        }
    }

    fn builtin_failed() -> Self {
        Self {
            resolved: true,
            failed: true,
            ..Self::default()
        }
    }

    fn resolved_skipped() -> Self {
        Self {
            resolved: true,
            skipped: true,
            ..Self::default()
        }
    }

    fn record(&self, macro_name: &str) {
        metric::MACRO_CALLS_SEEN.inc();

        if self.resolved {
            metric::MACRO_CALLS_RESOLVED.inc();
        }
        if self.unresolved {
            metric::MACRO_CALLS_UNRESOLVED.inc();
            metric::UNRESOLVED_BY_NAME.inc(macro_name);
        }
        if self.skipped {
            metric::MACRO_CALLS_SKIPPED.inc();
        }
        if self.expanded {
            metric::MACRO_CALLS_EXPANDED.inc();
        }
        if self.failed {
            metric::MACRO_CALLS_FAILED.inc();
            metric::MACRO_EXPAND_FAILURES.inc();
            metric::FAILED_EXPAND_BY_NAME.inc(macro_name);
        }

        if let Some(compile) = self.compile {
            match compile {
                MacroCompileRecord::CacheHit { failed } => {
                    metric::MACRO_COMPILE_CACHE_HITS.inc();
                    if failed {
                        metric::MACRO_CALLS_FAILED.inc();
                        metric::MACRO_COMPILE_FAILURES.inc();
                        metric::FAILED_COMPILE_BY_NAME.inc(macro_name);
                    }
                }
                MacroCompileRecord::Attempt { elapsed, failed } => {
                    metric::MACRO_COMPILE_ATTEMPTS.inc();
                    metric::TIMING_COMPILE_MACROS.record(elapsed);
                    if failed {
                        metric::MACRO_CALLS_FAILED.inc();
                        metric::MACRO_COMPILE_FAILURES.inc();
                        metric::FAILED_COMPILE_BY_NAME.inc(macro_name);
                    }
                }
            }
        }

        if let Some(expand) = self.expand {
            match expand {
                MacroExpandRecord::CacheHit { failed } => {
                    metric::MACRO_EXPAND_CACHE_HITS.inc();
                    if failed {
                        metric::MACRO_CALLS_FAILED.inc();
                        metric::MACRO_EXPAND_FAILURES.inc();
                        metric::FAILED_EXPAND_BY_NAME.inc(macro_name);
                    } else {
                        metric::MACRO_CALLS_EXPANDED.inc();
                    }
                }
                MacroExpandRecord::Attempt => {
                    metric::MACRO_EXPAND_ATTEMPTS.inc();
                }
            }
        }
    }
}

enum MacroExpansionAttemptOutcome {
    NoSource(MacroDirectiveState),
    Generated(ExpansionSyntax),
    ItemTreeFile(FileId),
    ItemTreeFragment {
        file_id: FileId,
        items: Vec<ItemTreeId>,
    },
    PendingExpansion(MacroExpansionWork),
}
